
	.global _start 						#声明全局符号，作为内核入口点
_start:
			la $t0 , go 					#加载go标签到地址t0 虚拟地址：0x9000000000200000
			jirl $r0 , $t0 , 0		#跳转到t0,返回地址保存到r0 mmu将虚拟地址转为真是物理地址
go:
			la $sp, kernel_init_stack #加载栈顶地址到栈指针寄存器 $sp，初始化内核栈
			b main            				# 无条件跳转到 main 函数，开始 C 语言初始化


	.fill 4096,1,0   					# 从0x200007开始分配4096字节到0x201007，分配 4096 字节（4 KiB）的栈空间，全部填充为 0
kernel_init_stack:  				# 栈顶标签，指向预留栈空间的末尾（栈向下增长，即向低地址增长）

